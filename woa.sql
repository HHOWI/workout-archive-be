CREATE TABLE WORKOUT_RECORDS (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- 고유 ID, 자동 증가
  EXERCISE_NAME VARCHAR2(100) NOT NULL,                  -- 운동 이름, NOT NULL
  WEIGHT NUMBER,                                         -- 운동 무게, NULL 허용
  REPS NUMBER NOT NULL,                                  -- 반복 횟수, NOT NULL
  RECORD_DATE DATE DEFAULT SYSDATE                       -- 기록 날짜, 기본값 SYSDATE
);



